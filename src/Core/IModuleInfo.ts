/**
 * Information of a Kanro module.
 *
 * @export
 * @interface IModuleInfo
 */
export interface IModuleInfo {
    /**
     * Name of this Kanro module.
     *
     * @type {string}
     * @memberOf IModuleInfo
     */
    name: string;
    /**
     * Version of this Kanro module.
     *
     * @type {string}
     * @memberOf IModuleInfo
     */
    version: string;
}